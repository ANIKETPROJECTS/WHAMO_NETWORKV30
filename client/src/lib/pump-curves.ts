// ── Helpers ───────────────────────────────────────────────────────────────────

function normalizeDeg(deg: number): number {
  let d = deg % 360;
  if (d < 0) d += 360;
  return d;
}

/** Prepare and sort a raw [angle, value] dataset, normalising angles to [0, 360). */
function prepareDataset(raw: [number, number][]): [number, number][] {
  const filtered = raw
    .filter(([a]) => Math.abs(a) <= 370)
    .map(([a, v]): [number, number] => [normalizeDeg(a), v]);

  filtered.sort((a, b) => a[0] - b[0]);

  // De-duplicate very close angles (keep first occurrence)
  const deduped: [number, number][] = [];
  for (let i = 0; i < filtered.length; i++) {
    if (i === 0 || Math.abs(filtered[i][0] - filtered[i - 1][0]) > 0.001) {
      deduped.push(filtered[i]);
    }
  }
  return deduped;
}

/** Linear interpolation between two dataset points with circular wraparound. */
function interpolateCircular(pts: [number, number][], thetaDeg: number): number {
  const theta = normalizeDeg(thetaDeg);

  // Find surrounding indices
  let lo = -1;
  for (let i = 0; i < pts.length - 1; i++) {
    if (theta >= pts[i][0] && theta <= pts[i + 1][0]) {
      lo = i;
      break;
    }
  }

  if (lo >= 0) {
    const [a0, v0] = pts[lo];
    const [a1, v1] = pts[lo + 1];
    if (Math.abs(a1 - a0) < 1e-9) return v0;
    return v0 + (v1 - v0) * (theta - a0) / (a1 - a0);
  }

  // Wraparound: interpolate between last and first point
  const last = pts[pts.length - 1];
  const first = pts[0];
  const span = (first[0] + 360) - last[0];
  if (span < 1e-9) return last[1];

  const t = theta >= last[0]
    ? theta - last[0]
    : (theta + 360) - last[0];
  return last[1] + (first[1] - last[1]) * t / span;
}

// ── Generation ────────────────────────────────────────────────────────────────

function buildRatioArray(min: number, max: number, step: number): number[] {
  const arr: number[] = [];
  let v = min;
  while (v <= max + 1e-9) {
    arr.push(parseFloat(v.toFixed(10)));
    v += step;
  }
  return arr;
}

export function generatePcharFromCurves(opts: GeneratePcharOptions): GeneratedPchar {
  const { nsGroup, sratioMin, sratioMax, sratioStep, qratioMin, qratioMax, qratioStep } = opts;

  const sratio = buildRatioArray(sratioMin, sratioMax, sratioStep);
  const qratio = buildRatioArray(qratioMin, qratioMax, qratioStep);

  const headPts = prepareDataset(nsGroup.headRaw);
  const torqPts = prepareDataset(nsGroup.torqueRaw);

  const CLAMP = 5.0;

  // hratio: [sratio.length rows][qratio.length cols]  (row = one speed ratio, col = one flow ratio)
  const hratio: number[][] = [];
  // tratio: flat, row-major (same indexing)
  const tratio: number[] = [];

  for (const s of sratio) {
    const hrow: number[] = [];
    for (const q of qratio) {
      const denom = s * s + q * q;
      if (denom < 1e-6) {
        hrow.push(0);
        tratio.push(0);
      } else {
        // theta = atan2(q, s) in degrees, mapped to [0, 360)
        const thetaDeg = normalizeDeg((Math.atan2(q, s) * 180) / Math.PI);

        const wh = interpolateCircular(headPts, thetaDeg);
        const wb = interpolateCircular(torqPts, thetaDeg);

        const hr = Math.max(-CLAMP, Math.min(CLAMP, wh * denom));
        const tr = Math.max(-CLAMP, Math.min(CLAMP, wb * denom));

        hrow.push(parseFloat(hr.toFixed(4)));
        tratio.push(parseFloat(tr.toFixed(4)));
      }
    }
    hratio.push(hrow);
  }

  return { sratio, qratio, hratio, tratio };
}

/** Format a GeneratedPchar as the PCHAR TYPE 3 INP block string. */
export function formatPcharBlock(pchar: GeneratedPchar, pType: number): string {
  const lines: string[] = [];
  lines.push(`PCHAR TYPE ${pType}`);
  lines.push(` SRATIO  ${pchar.sratio.map(v => v.toFixed(2)).join('  ')}`);
  lines.push(` QRATIO  ${pchar.qratio.map(v => v.toFixed(2)).join('  ')}`);
  lines.push(' HRATIO');
  for (const row of pchar.hratio) {
    lines.push('  ' + row.map(v => v.toFixed(4)).join('  '));
  }
  lines.push(' TRATIO');
  for (let i = 0; i < pchar.tratio.length; i += 8) {
    lines.push('  ' + pchar.tratio.slice(i, i + 8).map(v => v.toFixed(4)).join('  '));
  }
  lines.push('FINISH');
  return lines.join('\n');
}
