// Pump four-quadrant curve library — CircularFormat data
// Source: QuadrantCurvesPredefined_Pumps.txt (CircularFormat entries only)
// Format: [angle_degrees, WH_or_WB_value] pairs
// theta = atan2(Q_ratio, N_ratio), WH = H_ratio / (N²+Q²), WB = T_ratio / (N²+Q²)

export interface PumpNsGroup {
  nsUS: number;
  nsSI: number;
  label: string;
  headRaw: [number, number][];  // [angle_deg, WH]
  torqueRaw: [number, number][]; // [angle_deg, WB]
}

export interface GeneratePcharOptions {
  nsGroup: PumpNsGroup;
  sratioMin: number;
  sratioMax: number;
  sratioStep: number;
  qratioMin: number;
  qratioMax: number;
  qratioStep: number;
}

